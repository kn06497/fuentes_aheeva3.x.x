function scrollHere(scrollMe) { // scroll selected section to top of page
	var scrollFrom = document.getElementById("helpMain").scrollTop;
	var scrollHere = document.getElementById(scrollMe).offsetTop;
	var distanceMove = 50;
	if (scrollFrom>scrollHere) {
		for(var i=scrollFrom; i>scrollHere; i-=distanceMove) {
			if ((i-distanceMove)<scrollHere) {
				document.getElementById("helpMain").scrollTop = scrollHere-5;
			}else{
				document.getElementById("helpMain").scrollTop -= distanceMove;
			}
		}
	}else{
		for(var i=scrollFrom; i<scrollHere; i+=distanceMove) {
			if ((i+distanceMove)>scrollHere) {
				document.getElementById("helpMain").scrollTop = scrollHere-5;
			}else{
				document.getElementById("helpMain").scrollTop += distanceMove;
			}
		}
	}
}

