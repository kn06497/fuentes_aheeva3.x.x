function addHelpContent(searchFor) {
	numberTextResults = 0;
	textBeingSearched = searchFor;
	document.getElementById("btPreviou").style.visibility = "hidden";
//	document.getElementById("helpMenu_01").className = "helpsectionout";
//	document.getElementById("helpMenu_02").className = "helpsectionout";
	
	var textSearch = new RegExp(searchFor, "gi");
	document.getElementById("helpMain").innerHTML = "";
	var helpResults = "";
	for (var i=0; i<helpList.length; i++) {
		if (helpList[i].match("help"+searchFor) != null) { // if search for complete help section
			helpResults += helpList[i];
			document.getElementById("menuContent").value = searchFor;
		}
	}
	if (helpResults != "") {
//		document.getElementById("helpMenu_"+searchFor).className = "helpsectionclick";
		document.getElementById("helpMain").innerHTML = helpResults;
	}else{
		for (var j=0; j<helpList.length; j++) {
			if (helpList[j].search(textSearch) != -1) { // if text search
			
				if (helpList[j].match("_00") == null) { // ignore section headers
			
					var helpPrelimRresults = "";
					var hasTextResult = null;
					var helpSplit = helpList[j].split(/>/); // break positive results at end of tags and create an array
					for (k=0; k<helpSplit.length; k++) {
						if (helpSplit[k] != "") { // ignore empty array - firefox fix
							if (helpSplit[k].match(/^</)) { // if array is a tag - no change to code
								helpPrelimRresults += helpSplit[k]+">";
							}else{
								if (helpSplit[k].match(/</)) { // if array contains a tag
									var helpSplitMore = helpSplit[k].split(/</); //find begining of tag
									if (helpSplitMore[0]) { // ...is the text in array. Check for search entry and change code to emphasise searched text
										if (helpSplitMore[0].search(textSearch) != -1) {
											hasTextResult += 1; // notification of found text
										}
										helpPrelimRresults += helpSplitMore[0].replace(textSearch, "<span style='background-color:#CCCC00; padding-left:1px; padding-right:1px; padding-bottom:2px;'>"+searchFor+"</span>");
									}
									if (helpSplitMore[1]) { // ...is the tag in the array - no change to code
										helpPrelimRresults += "<"+helpSplitMore[1]+">";
									}
								}else{
									if (helpSplit[k].search(textSearch) != -1) { // when array contains no tags... check for search entry and change code to emphasise searched text
										hasTextResult += 1; // notification of found text
									}
								helpPrelimRresults += helpSplit[k].replace(textSearch, "<span style='background-color:#CCCC00; padding-left:1px; padding-right:1px; padding-bottom:2px;'>"+searchFor+"</span>")+">";
								}
							}
						}
					}
				}
				if (hasTextResult != null) { // if search in code has results in text and not in tags
					helpResults += helpPrelimRresults;
					numberTextResults++;
				}
			}
		}
		if (helpResults != "") {
			if(numberTextResults>0) {
				var notifyHelpResults = "<div style='width:400px; padding-left:20px; padding-top:10px;'>"+lab_h_arr[480]+" "+numberTextResults+" "+lab_h_arr[481]+" '"+searchFor+"'.</div>";
					notifyHelpResults += helpResults;
				document.getElementById("helpMain").innerHTML = notifyHelpResults;
			}else{
				document.getElementById("helpMain").innerHTML = helpResults;
			}
			document.getElementById("menuContent").value = 0;
		}else{ // no results found
			document.getElementById("helpMain").innerHTML = "<div style='width:400px; padding-left:20px; padding-top:10px;'>"+lab_h_arr[475]+".<br /><br />"+lab_h_arr[476]+" '"+searchFor+"' "+lab_h_arr[477]+".<br /><br />"+lab_h_arr[478]+".<br />"+lab_h_arr[479]+".</div>";
			document.getElementById("menuContent").value = 0;
		}
	}
	showFoundImages();
	document.getElementById("helpSearchEntry").value = "";
}

