function helpMenuMouseOver(whichParam){
	if (whichParam.className == "helpsectionout") {
		whichParam.className = "helpsectionover";
	}
	if (whichParam.className == "helpsearchout") {
		whichParam.className = "helpsearchover";
	}
}

function helpMenuMouseOut(whichParam){
	if (whichParam.className == "helpsectionover") {
		whichParam.className = "helpsectionout";
	}
	if (whichParam.className == "helpsearchover") {
		whichParam.className = "helpsearchout";
	}
}


function linkOverHelpTitle(thisParam) {
	if (thisParam.className=="helptitle") {
		thisParam.className = "helptitleover";
	}
	
	if (thisParam.className=="helptitleclick") {
		thisParam.className = "helptitleclickover";
	}
}
function linkOutHelpTitle(thisParam) {
	if (thisParam.className=="helptitleover") {
		thisParam.className = "helptitle";
	}
	
	if (thisParam.className=="helptitleclickover") {
		thisParam.className = "helptitleclick";
	}
}


function linkClickHelpTitle(thisParam) { // open and close help sections
	
	if (thisParam.className=="helptitleclick" || thisParam.className=="helptitleclickover") {
		thisParam.className = "helptitleover";
		thisParam.nextSibling.style.display = "none";
		if (thisParam.id == clickedId) { // if closing section with the anchor of an active jump link
			document.getElementById("btPreviou").style.visibility = "hidden"; // remove back to anchor
		}
	}else{
		clickedId = thisParam.id;
		thisParam.className = "helptitleclick";
		thisParam.nextSibling.style.display = "";
		document.getElementById("btPreviou").style.visibility = "hidden";
		scrollHere(clickedId);
	}	
}

